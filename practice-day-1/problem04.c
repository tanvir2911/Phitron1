#include <stdio.h>

int main()
{
    int a,b;

    printf("Enter two numbers: ");
    scanf("%d%d", &a, &b);

    printf("The summation of the numbers is %d\n", a+b);
    printf("The subtraction of the numbers is %d\n", a-b);
    printf("The multiplication of the numbers is %d\a", a*b);

    return 0;
}
